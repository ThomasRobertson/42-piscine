int	ft_iterative_power(int nb, int power)
{
	int	i;
	int	nbr;

	if (power < 0)
		return (0);
	else if (power == 0)
		return (1);
	i = 1;
	nbr = nb;
	while (i < power)
	{
		nb = nbr * nb;
		i++;
	}
	return (nb);
}
